namespace Categories
{
    public static class Category
    {
        #region Υπολογισμός Μορίων Εξωτερικού

        // ── Πώς λειτουργεί ──────────────────────────────────────────────
        // Η μοριοδότηση είναι ΑΡΝΗΤΙΚΗ και ΠΡΟΟΔΕΥΤΙΚΗ: οι μήνες υπηρεσίας
        // χωρίζονται σε 5 κατηγορίες (ζώνες) και κάθε μήνας μετράει με τον
        // συντελεστή της ζώνης στην οποία πέφτει — ΟΧΙ όλος ο χρόνος με έναν
        // ενιαίο συντελεστή.
        //
        //   Κατηγορία | Μήνες  | Μόρια/μήνα
        //   --------- | ------ | ----------
        //       1     |  0-6   |   -1
        //       2     |  6-12  |   -1,5
        //       3     | 12-24  |   -2
        //       4     | 24-36  |   -4
        //       5     | 36+    |   -6     (ανοιχτή — χωρίς άνω όριο)
        //
        // Παράδειγμα για 14 μήνες:
        //    6 μήνες * -1   = -6   (ζώνη 1)
        //    6 μήνες * -1,5 = -9   (ζώνη 2)
        //    2 μήνες * -2   = -4   (ζώνη 3)
        //    ──────────────────────────────
        //    σύνολο         = -19

        // Πλάτος κάθε ζώνης σε μήνες. Η τελευταία ζώνη (36+) είναι ανοιχτή,
        // γι' αυτό χρησιμοποιούμε int.MaxValue: "χωράει" όλους τους
        // υπόλοιπους μήνες όσοι κι αν είναι.
        private static readonly int[] CatMonths = { 6, 6, 12, 12, int.MaxValue };

        // Μόρια ανά μήνα για κάθε ζώνη (αρνητικά, ίδια σειρά με το CatMonths).
        private static readonly decimal[] CatRate = { -1m, -1.5m, -2m, -4m, -6m };

        /// <summary>
        /// Επιστρέφει τα (αρνητικά) μόρια για <paramref name="mhnes"/> μήνες
        /// υπηρεσίας στο εξωτερικό, υπολογισμένα προοδευτικά ανά ζώνη.
        /// </summary>
        public static decimal GetMoriaForEjoteriko(int mhnes)
        {
            decimal result = 0m;

            // Διατρέχουμε τις ζώνες με τη σειρά. Σε κάθε ζώνη "κόβουμε" όσους
            // μήνες χωράνε και τους χρεώνουμε με τον συντελεστή της. Μόλις
            // τελειώσουν οι μήνες (mhnes <= 0) σταματάμε.
            for (int i = 0; i < CatRate.Length && mhnes > 0; i++)
            {
                // Πόσοι μήνες πέφτουν σε αυτή τη ζώνη: ό,τι έχει απομείνει,
                // αλλά το πολύ όσο το πλάτος της ζώνης.
                int band = mhnes < CatMonths[i] ? mhnes : CatMonths[i];

                result += band * CatRate[i];  // χρέωση αυτής της ζώνης
                mhnes -= band;                // αφαίρεση όσων καταναλώθηκαν
            }

            return result;
        }

        #endregion
    }
}
